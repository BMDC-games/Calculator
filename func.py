def prime(num):
    for i in range(2,num/2 + 1):
        if num%i == 0: return False
        else: pass
    return True 
def power(num1,num2):
    return num1 ** num2
    

def devide(num1,num2): 
    return float(num1)/float(num2)
def times(num1,num2):
    return num1 * num2
def minus(num1,num2):
    return num1-num2
def add(num1,num2):
    return num1+num2

